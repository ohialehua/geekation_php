<?php
// 以下をfor文を使用して表示してください。

// 321
// 32
// 3

$n = 3;
for ($a = 1; $a <= 3; $a++) {
    for ($b = 3; $b >= $a; $b--) {
    //初期値は3つで、横列の数字の数が縦列の個数以上の間繰り返す
        echo $n;
        $n--;
    }
    echo "<br>";
    $n = 3;
    //変数の再設定
}

for ($a = 1; $a <= 3; $a++) {
    for ($b = $a; $b <= 3; $b++) {
        echo $n;
        $n--;
    }
    echo "<br>";
    $n = 3;
}
